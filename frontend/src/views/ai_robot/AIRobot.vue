<script>
import { computed, ref } from 'vue'
import { useRouter } from 'vue-router'
import gsap from 'gsap'

import Spinner from '../../components/Spinner.vue'
import { displayMsg, consoleLog } from '../../util/debug';

export default {
    name: 'Patterns',
    props: ['trace', 'userrole', 'role', 'currentuser', 'userID', 'superUser', 'role', 'connected',
        'workspaceID', 'workspace', 'projectID', 'projectName', 'subprojectID', 'subprojectName', 'location'],
    components: { Spinner },

    setup(props, context) {
        const router = useRouter()
        const trace = ref(props.trace)
        const actionAllowed = ref(true)

        displayMsg('Patterns.vue', trace.value)
        consoleLog('Patterns.vue - props', 1, props, trace.value)

        // -------------------------------------------
        // Check if the user is still connected
        // -------------------------------------------
        if (!props.connected) {
            router.push({ name: 'Login' })
            return
        }

        const workspaceID = ref(props.workspaceID)
        const workspace = ref(props.workspace)
        const projectID = ref(props.projectID)
        const projectName = ref(props.projectName)
        const subprojectID = ref(props.subprojectID)
        const subprojectName = ref(props.subprojectName)
        const userID = ref(props.userID)
        const superUser = ref(props.superUser)
        const pattern = ref([])
        const displayInfo = ref('')
        const location = ref(props.location)
        const filterValue = ref('')



        // -------------------------------------------
        // Management of the errors
        // -------------------------------------------
        const errorMessage = ref('')
        const styleMessage = ref('')

        // --------------------------------------------------------------------------
        // Execute a function from an error message
        // --------------------------------------------------------------------------
        const displayErrorFunction = (myCallback) => {
            errorMessage.value = ''
            if (myCallback != null && myCallback != undefined) {
                // call the function
                myCallback()
            }
        }

        // --------------------------------------------------------------------------
        // Display error message on the screen and trigger a function after a delay
        // --------------------------------------------------------------------------
        const DisplayError = (myMessage, myStyle, myCallback) => {
            errorMessage.value = myMessage
            styleMessage.value = myStyle.toLowerCase()
            consoleLog('Patterns.vue/DisplayError', 2, 'Message: ' + errorMessage.value + ', Style: ' + styleMessage.value, trace.value)
            if (myStyle != 'Alert') {
                setTimeout(() => displayErrorFunction(myCallback), 3000)
            }
        }

        // ----------------------------------------------------------------------------
        // User clicks on a menu, open the corresponding component 
        // ----------------------------------------------------------------------------
        const GotoScreen = (pageName) => {
            consoleLog('Patterns.vue/GotoScreen', 2, 'pageName: ' + pageName, trace.value)
            context.emit('storelocation', 'controlpanel')
            router.push({ name: pageName })
        }

        // ----------------------------------------------------------------------------
        // Transition functions
        // ----------------------------------------------------------------------------
        const beforeEnter = (el) => {
            el.style.opacity = 0
            el.style.transform = 'translateY(100px)'
        }

        const enter = (el, done) => {
            gsap.to(el, {
                opacity: 1,
                y: 0,
                duration: 0.3,
                onComplete: done,
                delay: el.dataset.index * 0.1
            })
        }
        // ----------------------------------------------------------------------------
        // Setup the icons for the menu
        // ----------------------------------------------------------------------------
        const icons = ref([
            { class: 'fa-solid fa-chart-line', text: 'Dashboard', name: 'AIDashboard' },
            { class: 'fa-solid fa-briefcase-medical', text: 'Patterns', name: 'Patterns' },
            //{ class: 'fa-solid fa-chalkboard-user', text: 'Training', name: 'AITraining' },
            //{ class: 'fa-solid fa-table-cells', text: 'Tag Attribute', name: 'Tag Attribute' },
            //{ class: 'fa-solid fa-chart-line', text: 'Statistics', name: 'Statistics' },
            { class: 'fa-solid fa-object-ungroup', text: 'Selector', name: 'AISelector' },
            { class: 'fa-solid fa-magnifying-glass', text: 'Attribute', name: 'AIAttribute' },
            { class: 'fa-solid fa-road', text: 'Path', name: 'AIPath' },
        ])

        // --------------------------------------------------------------------------
        // User cancel the action, Back to the control panel or the dashbaord
        // --------------------------------------------------------------------------
        const handleCancel = () => {
            consoleLog('Patterns.vue/handleCancel', 2, 'User Cancel the action', trace.value)
            router.push({ name: 'ControlPanel' })
        }



        return {
            errorMessage, styleMessage, workspaceID, workspace, actionAllowed, superUser, projectID,
            projectName, subprojectName, userID, displayInfo, trace, icons,
            handleCancel, beforeEnter, enter, GotoScreen
        }


    }

}
</script>

