                    <transition-group appear tag="ul" @before-enter="beforeEnter" @enter="enter">
                        <li v-for="(icon, index) in icons" :key="icon.name99" :data-index="index"
                            @click="GotoScreen(icon.name)">
                            <i :class="icon.class"></i>
